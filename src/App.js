import { useState } from "react";
import "./index.css";

export default function App() {
  const refreshPage = () => {
    window.location.reload();
  };
  return (
    <div className="container">
      <button className="re" onClick={refreshPage}>
        Refresh
      </button>
      <App1 title="الاستغفار" messages={istighfarMessages} />
      <App1 title="التحميد" messages={tahmeedMessages} />
      <App1 title="التكبير" messages={takbeerMessages} />
      <Footer />
    </div>
  );
}

function App1({ title, messages }) {
  const [index, setIndex] = useState(0);

  const nextMessage = () => {
    if (index < messages.length - 1) {
      setIndex((prev) => prev + 1);
    }
  };

  return (
    <div className="app">
      <h1>{title}</h1>
      <p>{messages[index]}</p>
      <button onClick={nextMessage} disabled={index === messages.length - 1}>
        +
      </button>
    </div>
  );
}

